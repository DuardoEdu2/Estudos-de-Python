# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro

"""
O tipo de dado boolean é a ferramenta utilizada para
analizar se resultados são verdadeiros ou falsos.
Ele é utilizados como operadores logicos para obter os estado
de um resultado como numa equaação ou função, se esta certo ou errado
verdadeiro ou falso.
"""

